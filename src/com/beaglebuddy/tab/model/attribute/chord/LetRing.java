package com.beaglebuddy.tab.model.attribute.chord;




/**
 * This class represents a let ring chord attribute.
 */
public class LetRing extends ChordAttribute
{
   /**
    * default constructor.
    */
   public LetRing()
   {
      super(ChordAttribute.Type.LetRing);
   }

   /**
    * @return a new copy of the let ring chord attribute.
    */
   @Override
   public ChordAttribute clone()
   {
      return new LetRing();
   }
}
