package com.beaglebuddy.tab.model.attribute.chord;




/**
 * This class represents a triplet feel 2nd chord attribute.
 */
public class TripletFeel2nd extends ChordAttribute
{
   /**
    * default constructor.
    */
   public TripletFeel2nd()
   {
      super(ChordAttribute.Type.TripletFeel2nd);
   }

   /**
    * @return a new copy of the triplet feel 2nd chord attribute.
    */
   @Override
   public ChordAttribute clone()
   {
      return new TripletFeel2nd();
   }
}
