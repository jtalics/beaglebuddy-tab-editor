package com.beaglebuddy.tab.model.attribute.chord;




/**
 * This class represents a tap chord attribute.
 */
public class Tap extends ChordAttribute
{
   /**
    * default constructor.
    */
   public Tap()
   {
      super(ChordAttribute.Type.Tap);
   }

   /**
    * @return a new copy of the tap chord attribute.
    */
   @Override
   public ChordAttribute clone()
   {
      return new Tap();
   }
}
