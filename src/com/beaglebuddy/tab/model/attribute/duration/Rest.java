package com.beaglebuddy.tab.model.attribute.duration;




/**
 * This class represents a beaglebuddy tab rest duration attribute.
 */
public class Rest extends DurationAttribute
{
   /**
    * default constructor.
    */
   public Rest()
   {
      super(DurationAttribute.Type.Rest);
   }

   /**
    * @return a new copy of the rest duration attribute.
    */
   @Override
   public DurationAttribute clone()
   {
      return new Rest();
   }
}
