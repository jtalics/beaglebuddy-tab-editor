package com.beaglebuddy.tab.model.attribute.duration;




/**
 * This class represents a double dotted duration attribute.
 */
public class DottedDouble extends DurationAttribute
{
   /**
    * default constructor.
    */
   public DottedDouble()
   {
      super(DurationAttribute.Type.DottedDouble);
   }

   /**
    * @return a new copy of the double dotted duration attribute.
    */
   @Override
   public DurationAttribute clone()
   {
      return new DottedDouble();
   }
}
