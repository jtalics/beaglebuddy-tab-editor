package com.beaglebuddy.tab.model.attribute.rhythm;




/**
 * This class represents a beaglebuddy tab muted rhythm slash attribute.
 */
public class Muted extends RhythmSlashAttribute
{
   /**
    * default constructor.
    */
   public Muted()
   {
      super(RhythmSlashAttribute.Type.Muted);
   }

   /**
    * @return a new copy of the muted rhythm slash attribute.
    */
   @Override
   public RhythmSlashAttribute clone()
   {
      return new Muted();
   }
}
