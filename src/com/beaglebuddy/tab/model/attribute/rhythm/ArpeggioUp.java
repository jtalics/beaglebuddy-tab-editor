package com.beaglebuddy.tab.model.attribute.rhythm;




/**
 * This class represents a beaglebuddy tab arpeggio up rhythm slash attribute.
 */
public class ArpeggioUp extends RhythmSlashAttribute
{
   /**
    * default constructor.
    */
   public ArpeggioUp()
   {
      super(RhythmSlashAttribute.Type.ArpeggioUp);
   }

   /**
    * @return a new copy of the arpeggio up rhythm slash attribute.
    */
   @Override
   public RhythmSlashAttribute clone()
   {
      return new ArpeggioUp();
   }
}
