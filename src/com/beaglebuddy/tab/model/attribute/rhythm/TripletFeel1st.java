package com.beaglebuddy.tab.model.attribute.rhythm;




/**
 * This class represents a beaglebuddy tab triplet feel 1st rhythm slash attribute.
 */
public class TripletFeel1st extends RhythmSlashAttribute
{
   /**
    * default constructor.
    */
   public TripletFeel1st()
   {
      super(RhythmSlashAttribute.Type.TripletFeel1st);
   }

   /**
    * @return a new copy of the 1st triplet feel rhythm slash attribute.
    */
   @Override
   public RhythmSlashAttribute clone()
   {
      return new TripletFeel1st();
   }
}
