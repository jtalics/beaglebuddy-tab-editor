package com.beaglebuddy.tab.gui.mainframe;

import com.beaglebuddy.tab.model.Location;



/**
 * this interface is for listening for movement and highlighting of the cursor within the song.
 * whenever the user moves the cursor, or highlights an object on the screen, objects that have registered as a cursor listener will be notified.
 */
public interface CursorListener
{
   /**
    * invoked whenever the cursor is moved or an object is highlighted in the song.
    * <br/><br/>
    * @param location   the location of the cursor within the song.
    */
   public void cursor(Location location);
}
